SELECT CASE 
    WHEN S9.PLAYER IS NOT NULL
        THEN S9.PLAYER
        WHEN S9.PLAYER IS NULL
            THEN COALESCE(S10.PLAYER,S11.PLAYER)
            WHEN S10.PLAYER IS NULL
                THEN COALESCE(S11.PLAYER,S10.PLAYER)
    ELSE COALESCE(S10.PLAYER,S9.PLAYER)
END as NAME,
COALESCE(S9.TD,0) AS TD9,COALESCE(S10.TD,0) AS TD10,
COALESCE(S11.TD,0) AS TD11,
(COALESCE(S9.TD,0)+COALESCE(S10.TD,0)+COALESCE(S11.TD,0)) AS TOTAL_TD FROM NFL.S2019 AS S9
LEFT JOIN NFL.S2021 AS S11
ON S11.PLAYER = S9.PLAYER
LEFT JOIN NFL.S2020 AS S10
ON S10.PLAYER = S11.PLAYER


UNION

SELECT CASE 
    WHEN S9.PLAYER IS NOT NULL
        THEN S9.PLAYER
        WHEN S9.PLAYER IS NULL
            THEN COALESCE(S10.PLAYER,S11.PLAYER)
            WHEN S10.PLAYER IS NULL
                THEN COALESCE(S11.PLAYER,S10.PLAYER)
    ELSE COALESCE(S10.PLAYER,S9.PLAYER)
END as NAME,
COALESCE(S9.TD,0) AS TD9,COALESCE(S10.TD,0) AS TD10,
COALESCE(S11.TD,0) AS TD11,
(COALESCE(S9.TD,0)+COALESCE(S10.TD,0)+COALESCE(S11.TD,0)) AS TOTAL_TD FROM NFL.S2019 AS S9
RIGHT JOIN NFL.S2021 AS S11
ON S11.PLAYER = S9.PLAYER
RIGHT JOIN NFL.S2020 AS S10
ON S10.PLAYER = S11.PLAYER


